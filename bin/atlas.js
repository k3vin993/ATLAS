#!/usr/bin/env node
/**
 * ATLAS CLI (ATLAS-15)
 * atlas init       — interactive setup wizard
 * atlas validate   — validate config.yml
 * atlas seed       — load data from files  
 * atlas status     — show sync status
 * atlas start      — start HTTP server
 */
import { readFileSync, writeFileSync, existsSync, readdirSync, mkdirSync } from 'fs';
import { join } from 'path';
import { createInterface } from 'readline';

const [,, command, ...args] = process.argv;
const cwd = process.cwd();

async function ask(rl, q) { return new Promise(r => rl.question(q, r)); }

async function cmdInit() {
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  console.log('\n🗺️  ATLAS Setup Wizard\n');
  const name    = await ask(rl, 'Deployment name: ');
  const port    = await ask(rl, 'HTTP port [3000]: ') || '3000';
  const dbPath  = await ask(rl, 'SQLite path [./atlas.db]: ') || './atlas.db';
  const retDays = await ask(rl, 'Retention days [90]: ') || '90';
  rl.close();
  const config = `# ATLAS config.yml — generated by atlas init
atlas:
  name: "${name}"
  port: ${port}

storage:
  path: "${dbPath}"
  retention_days: ${retDays}

auth:
  tokens: []

models:
  # Enable domain extensions:
  # asset: true
  # tender: true
  # issue: true

connectors: []
webhooks: []
`;
  writeFileSync(join(cwd, 'config.yml'), config);
  console.log('\n✅ config.yml created');
  console.log('   node src/ui-server.js   (HTTP + MCP + REST)');
  console.log('   node src/index.js        (stdio MCP)\n');
}

async function cmdValidate() {
  const path = args[0] ?? join(cwd, 'config.yml');
  if (!existsSync(path)) { console.error(`❌ Not found: ${path}`); process.exit(1); }
  const YAML = (await import('yaml')).default;
  const cfg  = YAML.parse(readFileSync(path, 'utf8'));
  const errs = [];
  if (!cfg?.atlas?.port)   errs.push('atlas.port missing');
  if (!cfg?.storage?.path) errs.push('storage.path missing');
  for (const c of (cfg?.connectors ?? [])) {
    if (!c.id)   errs.push(`connector missing id`);
    if (!c.type) errs.push(`connector "${c.id}" missing type`);
    if (c.type === 'rest_api' && !c.endpoint) errs.push(`connector "${c.id}" missing endpoint`);
  }
  if (errs.length) { console.error('❌ Errors:\n' + errs.map(e => '  - ' + e).join('\n')); process.exit(1); }
  console.log(`✅ Valid — port:${cfg.atlas.port} db:${cfg.storage.path} connectors:${(cfg.connectors??[]).length}`);
}

async function cmdSeed() {
  const dir = args[0] ?? join(cwd, 'seed');
  if (!existsSync(dir)) { console.error(`❌ Not found: ${dir}`); process.exit(1); }
  const { Atlas } = await import('../src/atlas.js');
  const { ConnectorRunner } = await import('../src/connector-runner.js');
  const { statSync } = await import('fs');
  const atlas = new Atlas();
  try { atlas.loadConfig(); } catch {}
  atlas.initDb();
  const runner = new ConnectorRunner(atlas, atlas.config ?? {});

  // Recursive file walk
  function walkFiles(folder, results = []) {
    for (const entry of readdirSync(folder)) {
      const full = join(folder, entry);
      if (statSync(full).isDirectory()) {
        walkFiles(full, results);
      } else if (/\.(json|csv|xlsx?|md)$/i.test(entry)) {
        results.push(full);
      }
    }
    return results;
  }

  const files = walkFiles(dir);
  console.log(`\n🌱 Seeding ${files.length} files from ${dir}\n`);
  let total = 0;
  for (const filepath of files) {
    const file   = filepath.replace(dir + '/', '').replace(dir + '\\', '');
    const base   = file.split('/').pop().split('\\').pop(); // filename only
    const entity = base.replace(/[-_]\d+/g, '').replace(/\.[^.]+$/, '');
    const rows   = await runner._parseFile(filepath);
    if (!rows.length) { console.log(`   ${file.padEnd(38)} → (empty, skipped)`); continue; }
    const n = runner._upsertRows({ id: 'seed', mapping: null }, entity, rows);
    console.log(`   ${file.padEnd(38)} → ${entity}: ${n} records`);
    total += n;
  }
  console.log(`\n✅ Seed complete — ${total} total records\n`);
}

async function cmdStatus() {
  const { Atlas } = await import('../src/atlas.js');
  const atlas = new Atlas();
  try { atlas.loadConfig(); } catch {}
  atlas.initDb();
  console.log('\n📊 ATLAS Status\n');
  for (const [t, v] of Object.entries(atlas.getSyncStatus()))
    console.log(`  ${t.padEnd(22)} ${String(v.count).padStart(7)} records   ${v.last_synced_at ? 'synced: ' + v.last_synced_at : 'never synced'}`);
  console.log();
}

switch (command) {
  case 'init':     await cmdInit();     break;
  case 'validate': await cmdValidate(); break;
  case 'seed':     await cmdSeed();     break;
  case 'status':   await cmdStatus();   break;
  case 'start':    await import('../src/ui-server.js'); break;
  default:
    console.log(`
ATLAS CLI v1.0.0

  atlas init              Interactive setup wizard
  atlas validate          Validate config.yml
  atlas seed [./seed/]    Load data from JSON / CSV / XLSX files
  atlas status            Show record counts and sync timestamps
  atlas start             Start HTTP server (Web UI + MCP + REST)
`);
}
